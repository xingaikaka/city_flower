List<CityFlower> searchFlowers(String query) {
  if (query.isEmpty) return cityFlowers;
  return cityFlowers.where((f) =>
    f.cityName.contains(query) ||
    f.flowerName.contains(query) ||
    f.flowerAlias.contains(query) ||
    f.province.contains(query) ||
    f.meaning.contains(query)
  ).toList();
}

List<CityFlower> flowersByProvince(String province) {
  if (province == '全部') return cityFlowers;
  return cityFlowers.where((f) => f.province == province).toList();
}
